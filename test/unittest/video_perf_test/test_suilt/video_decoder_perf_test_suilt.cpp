/*  Generate case name
 *
 *  Example:
 *  GENERATE_CASE_NAME(FRAME_DELAY, BUFFER_SHARED_MEMORY, AVC, 1280, 720, 30, 10)
 *
 *  To be:
 *  CASE_FRAME_DELAY_BUFFER_SHARED_MEMORY_AVC_1280_720_30_10M
 */
#define GENERATE_CASE_NAME(testMode, codecRunMode, mime, width, height, fps, bitrate)                               \
    CASE_##testMode##_##codecRunMode##_##mime##_##width##_##height##_##fps##_##bitrate##M

/*  Template of perf test case
 *
 *  Example:
 *  ADD_CASE(FRAME_DELAY, BUFFER_SHARED_MEMORY, AVC, 1280, 720, 30, 10, FRAME_INTERVAL_33MS)
 *
 *  To be:
 *  HWTEST_F(VideoDecoderPerfTestSuilt, FRAME_DELAY_BUFFER_SHARED_MEMORY_AVC_1280_720_30_10M, TestSize.Level1)
 *  {
 *      SampleInfo sampleInfo = {
 *          FILE_AVC_1280_720_30_10M, MIME_VIDEO_AVC, 1280, 720, 30, BITRATE_10M,
 *          CodecRunMode::BUFFER_SHARED_MEMORY, TestMode::FRAME_DELAY, FRAME_INTERVAL_33MS
 *      };
 *      ASSERT_EQ(AVCODEC_SAMPLE_ERR_OK, RunTest(sampleInfo));
 *  }
 */
#define ADD_CASE(testMode, codecRunMode, mime, width, height, fps, bitrate, inteval)                                \
HWTEST_F(VideoDecoderPerfTestSuilt,                                                                                 \
GENERATE_CASE_NAME(testMode, codecRunMode, mime, width, height, fps, bitrate), TestSize.Level1)                     \
{                                                                                                                   \
    SampleInfo sampleInfo = {                                                                                       \
        FILE_##mime##_##width##_##height##_##fps##_##bitrate##M,                                                    \
        MIME_VIDEO_##mime, (width), (height), (fps), BITRATE_##bitrate##M, (codecRunMode), (testMode), (inteval)    \
    };                                                                                                              \
    ASSERT_EQ(AVCODEC_SAMPLE_ERR_OK, RunTest(sampleInfo));                                                          \
}

int32_t VideoDecoderPerfTestSuilt::RunTest(const SampleInfo &sampleInfo)
{
    AVCODEC_LOGI("====== Video perf test config ======");
    AVCODEC_LOGI("test mode: %{public}d, codec run mode: %{public}d", sampleInfo.testMode, sampleInfo.codecRunMode);
    AVCODEC_LOGI("input file: %{public}s", sampleInfo.inputFilePath.data());
    AVCODEC_LOGI("mime: %{public}s, %{public}d*%{public}d, %{public}.1ffps, %{public}.2fMbps, interval: %{public}dms",
        sampleInfo.codecMime.data(), sampleInfo.videoWidth, sampleInfo.videoHeight, sampleInfo.frameRate,
        static_cast<double>(sampleInfo.bitrate) / 1024 / 1024, FRAME_INTERVAL_33MS); // 1024: precision
    AVCODEC_LOGI("====== Video perf test config ======");

    std::unique_ptr<VideoDecoderPerfTestSample> sample = std::make_unique<VideoDecoderPerfTestSample>();
    int32_t ret = sample->Create(sampleInfo);
    CHECK_AND_RETURN_RET_LOG(ret == AVCODEC_SAMPLE_ERR_OK, AVCODEC_SAMPLE_ERR_ERROR, "Create failed");
    ret = sample->Start();
    CHECK_AND_RETURN_RET_LOG(ret == AVCODEC_SAMPLE_ERR_OK, AVCODEC_SAMPLE_ERR_ERROR, "Start failed");
    ret = sample->WaitForDone();
    CHECK_AND_RETURN_RET_LOG(ret == AVCODEC_SAMPLE_ERR_OK, AVCODEC_SAMPLE_ERR_ERROR, "Wait for done failed");
    return AVCODEC_SAMPLE_ERR_OK;
}

